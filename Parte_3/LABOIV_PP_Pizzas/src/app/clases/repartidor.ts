export class Repartidor {
    dni: any;
    nombre: any;
    edad: any;
    capacidad: any;
    pais: any;
    unidad: any;
    continente: any;

    constructor(dni: any = '',
                nombre: any ='',
                edad: any = '',
                capacidad: any = '',
                pais: any = '',
                unidad: any = '',
                continente: any = ''){
        this.dni = dni;
        this.nombre = nombre;
        this.edad = edad;
        this.capacidad = capacidad;
        this.pais = pais;
        this.unidad= unidad;
        this.continente = continente;
        }






}
