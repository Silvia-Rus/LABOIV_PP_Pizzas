export class Pizza {
    
    nombre: any;
    ingredientes: any;
    precio: any;
    peso: any;

    constructor(nombre: any,
                ingredientes: any,
                precio: any,
                peso: any){
            this.nombre = nombre;
            this.ingredientes = ingredientes;
            this.precio = precio;
            this.peso = peso;
        }
      
}
